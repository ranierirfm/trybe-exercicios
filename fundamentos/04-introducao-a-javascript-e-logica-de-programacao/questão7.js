// screva um programa que converte uma nota dada em 
// porcentagem (de 0 a 100) em conceitos de A a F. Siga essas regras:

let notaFinal = 30;

if (notaFinal >= 90) {
    console.log("A")
} else if (notaFinal >= 80) {
    console.log("B")
} else if (notaFinal >= 70) {
    console.log("C")
} else if (notaFinal >= 60) {
    console.log("D")
} else if (notaFinal >= 50) {
    console.log("E")
} else if (notaFinal < 50) {
    console.log("F")
}